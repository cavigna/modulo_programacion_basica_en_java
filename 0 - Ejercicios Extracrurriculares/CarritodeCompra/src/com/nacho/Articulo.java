package com.nacho;

public class Articulo {
    double precio;
    String nombre;
    String codigo;
    int talla;
    String marca;

    public Articulo(double precio, String nombre, String codigo, int talla, String marca) {
        this.precio = precio;
        this.nombre = nombre;
        this.codigo = codigo;
        this.talla = talla;
        this.marca = marca;
    }
}
